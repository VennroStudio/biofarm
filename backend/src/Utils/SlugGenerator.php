<?php

declare(strict_types=1);

namespace App\Utils;

final class SlugGenerator
{
    /**
     * Генерирует slug из строки с транслитерацией кириллицы
     */
    public static function generate(string $text): string
    {
        // Таблица транслитерации
        $translit = [
            'а' => 'a', 'б' => 'b', 'в' => 'v', 'г' => 'g', 'д' => 'd', 'е' => 'e', 'ё' => 'yo',
            'ж' => 'zh', 'з' => 'z', 'и' => 'i', 'й' => 'y', 'к' => 'k', 'л' => 'l', 'м' => 'm',
            'н' => 'n', 'о' => 'o', 'п' => 'p', 'р' => 'r', 'с' => 's', 'т' => 't', 'у' => 'u',
            'ф' => 'f', 'х' => 'h', 'ц' => 'ts', 'ч' => 'ch', 'ш' => 'sh', 'щ' => 'sch',
            'ъ' => '', 'ы' => 'y', 'ь' => '', 'э' => 'e', 'ю' => 'yu', 'я' => 'ya',
            'А' => 'A', 'Б' => 'B', 'В' => 'V', 'Г' => 'G', 'Д' => 'D', 'Е' => 'E', 'Ё' => 'Yo',
            'Ж' => 'Zh', 'З' => 'Z', 'И' => 'I', 'Й' => 'Y', 'К' => 'K', 'Л' => 'L', 'М' => 'M',
            'Н' => 'N', 'О' => 'O', 'П' => 'P', 'Р' => 'R', 'С' => 'S', 'Т' => 'T', 'У' => 'U',
            'Ф' => 'F', 'Х' => 'H', 'Ц' => 'Ts', 'Ч' => 'Ch', 'Ш' => 'Sh', 'Щ' => 'Sch',
            'Ъ' => '', 'Ы' => 'Y', 'Ь' => '', 'Э' => 'E', 'Ю' => 'Yu', 'Я' => 'Ya',
        ];

        // Транслитерация
        $slug = strtr($text, $translit);
        
        // Приводим к нижнему регистру
        $slug = mb_strtolower($slug, 'UTF-8');
        
        // Удаляем все символы кроме букв, цифр, пробелов и дефисов
        $slug = preg_replace('/[^\w\s-]/u', '', $slug);
        
        // Заменяем пробелы и множественные дефисы на один дефис
        $slug = preg_replace('/[-\s]+/', '-', $slug);
        
        // Убираем дефисы в начале и конце
        $slug = trim($slug, '-');
        
        return $slug;
    }
}
